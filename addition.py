def addition(a, b):
    """
    Cette fonction additionne deux chiffres.

    Arguments :
    a (int, float) : Le premier chiffre.
    b (int, float) : Le deuxième chiffre.

    Retourne :
    int, float : La somme des deux chiffres.
    """
    return a + b

# Exemple d'utilisation de la fonction
resultat = addition(5, 3)
print("Résultat de l'addition :", resultat)
